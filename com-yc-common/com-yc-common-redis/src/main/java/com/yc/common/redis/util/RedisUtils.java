    /**
     * 以给定的经纬度为中心， 返回键包含的位置元素当中， 与中心的距离不超过给定最大距离的所有位置元素，并给出所有位置元素与中心的平均距离。
     * @param key Gis数据在redis中的key
     * @param point  Point point = new Point(x,y);
     * @param distance Distance distance = new Distance(1, Metrics.KILOMETERS);
     * @param limit 匹配多少个
     * @return
     */
    public GeoResults<RedisGeoCommands.GeoLocation<Object>> geoRadiusByXY(String key,Point point, Distance distance,long limit){
        Circle circle = new Circle(point, distance);//Point(经度, 纬度) Distance(距离量, 距离单位)
        RedisGeoCommands.GeoRadiusCommandArgs args = RedisGeoCommands.GeoRadiusCommandArgs.newGeoRadiusArgs().includeDistance().includeCoordinates().sortAscending().limit(limit);
        return redisTemplate.opsForGeo().radius(key,circle,args);	//params: key, Circle, GeoRadiusCommandArgs
    }
    /**
     * 以给定的地名为中心， 返回键包含的位置元素当中， 与中心的距离不超过给定最大距离的所有位置元素，并给出所有位置元素与中心的平均距离。
     * @param key Gis数据在redis中的key
     * @param distance Distance distance = new Distance(1, Metrics.KILOMETERS);
     * @param limit 匹配多少个
     * @return
     */
    public GeoResults<RedisGeoCommands.GeoLocation<Object>> geoRadiusByPlace(String key,String place,Distance distance,long limit){
        RedisGeoCommands.GeoRadiusCommandArgs args = RedisGeoCommands.GeoRadiusCommandArgs.newGeoRadiusArgs().includeDistance().includeCoordinates().sortAscending().limit(limit);
        return redisTemplate.opsForGeo().radius(key,place,distance,args);//params: key, 地方名称, Circle, GeoRadiusCommandArgs
    }
