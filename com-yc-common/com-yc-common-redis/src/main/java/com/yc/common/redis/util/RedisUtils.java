    /*
     * redis分布式锁实现，同时利用expire设置过期时间
     * 返回true就是设置成功
     * */
    public Boolean setNX(String key, String value,long timeout, TimeUnit unit){
        Boolean isExit = this.redisTemplate.getConnectionFactory().getConnection().setNX(key.getBytes(), value.getBytes());
        //如果设置成功，要设置其过期时间
        if(isExit){
            redisTemplate.expire(key, timeout, unit);
        }
        return isExit;
    }

    /**
     * 自旋获取锁
     * @param key	键
     * @param value 分布式id，一般采用uuid
     * @param maxTime 最大自旋时间
     * @param expireTime 锁失效时间
     * @return
     */
    public Boolean casLock(String key,String value,long maxTime,long expireTime,TimeUnit unit){
        Long start = System.currentTimeMillis();
        for (;;){//一定时间内自旋获取锁，超时则返回加锁失败
            Boolean ret = redisTemplate.opsForValue().setIfAbsent(key, value,expireTime,unit);
            if (ret) {//获取锁成功
                return true;
            }
            //在maxTime毫秒的时间内仍未获取到锁，则获取失败
            long end = System.currentTimeMillis() - start;
            if (end >= maxTime){
                return false;
            }
        }
    }
    /**
     * 解锁
     * @param key	键
     * @param value 分布式唯一id，一般采用uuid
     * @return
     */
    public Boolean unLock(String key,String value){
        String redisValue = redisTemplate.opsForValue().get(key).toString();
        if(redisValue.equals(value)){
            redisTemplate.delete(key);
            return true;
        }
        return false;
    }

